    private void BotonVectorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonVectorActionPerformed
        try{
        int[] vector = new int[10];

        JOptionPane.showMessageDialog(null, "Ingresa 10 elementos numéricos:");

        for (int i = 0; i < vector.length; i++) {
            String input = JOptionPane.showInputDialog("Elemento " + (i + 1) + ":");
            vector[i] = Integer.parseInt(input);
        }

        JOptionPane.showMessageDialog(null, "Elementos precedidos por un cero:");

        for (int i = 1; i < vector.length; i++) {
            if (vector[i - 1] == 0) {
                for(int v = i; v< vector.length; v++){
                JOptionPane.showMessageDialog(null, vector[v]);}
            }
        }

        }catch(NumberFormatException a){
            JOptionPane.showMessageDialog(null, "Error: el valor ingresado no es numérico.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BotonVectorActionPerformed

    private void BotonVector1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonVector1ActionPerformed
        try{
            int n = 10; // Tamaño del vector
            int[] vector = new int[n];

            vector[0] = 1; // Valor inicial

            for (int i = 1; i < n; i++) {
                vector[i] = i * vector[i - 1] + i * i;
            }

            StringBuilder message = new StringBuilder("Elementos del vector calculados:\n");

            for (int i = 0; i < n; i++) {
                message.append("A[").append(i + 1).append("] = ").append(vector[i]).append("\n");
            }

        JOptionPane.showMessageDialog(null, message.toString());
        }catch(NumberFormatException a){
            JOptionPane.showMessageDialog(null, "Error: el valor ingresado no es numérico.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BotonVector1ActionPerformed

