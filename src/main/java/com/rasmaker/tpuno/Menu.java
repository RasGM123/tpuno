    private void BotonVector4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonVector4ActionPerformed
        try{
        int[][] matriz = new int[5][10];
        int[] sumaFilas = new int[5];

        // Leer valores de la matriz
        for(int i=0; i<matriz.length; i++) {
          for(int j=0; j<matriz[i].length; j++) {
            matriz[i][j] = Integer.parseInt(JOptionPane.showInputDialog("Fila " + (i+1) + " Columna " + (j+1))); 
          }
        }

        // Calcular suma de cada fila
        for(int i=0; i<matriz.length; i++) {
          int suma = 0;
          for(int j=0; j<matriz[i].length; j++) {
            suma += matriz[i][j];  
          }
          sumaFilas[i] = suma;
        }
        // Calcular y obtener suma de filas

        String mensaje = "Suma de filas: ";
        for(int num: sumaFilas) {
          mensaje += num + " "; 
        }

        JOptionPane.showMessageDialog(null, mensaje);
        }catch(NumberFormatException a){
            JOptionPane.showMessageDialog(null, "Error: el valor ingresado no es numérico.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BotonVector4ActionPerformed


